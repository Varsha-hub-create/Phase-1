function App() {
   
function multiply(a, b) {
  return a * b;
}

const divide = function(a, b) {
  return a / b;
};

const power = (base, exponent) => base ** exponent;

console.log("Multiply (3, 4):", multiply(3, 4)); // 12
console.log("Divide (10, 2):", divide(10, 2));   // 5
console.log("Power (2, 3):", power(2, 3));       // 8
}
  
  export default App; 
  